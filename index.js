const express = require('express');
const { Client, LocalAuth } = require('whatsapp-web.js');
const qrcode = require('qrcode-terminal');
const cors = require('cors');

const app = express();
const PORT = process.env.PORT || 3000;

// Middleware
app.use(express.json());
app.use(cors());

let clientReady = false;
let whatsappClient = null;

// Fun√ß√£o para descrever status de entrega
function getAckDescription(ack) {
    switch(ack) {
        case 0: return 'Erro';
        case 1: return 'Enviado ao servidor';
        case 2: return 'Recebido pelo destinat√°rio';
        case 3: return 'Lido pelo destinat√°rio';
        case 4: return 'Tocado (apenas √°udio)';
        default: return 'Status desconhecido';
    }
}

// Configura√ß√£o do cliente WhatsApp
const client = new Client({
    authStrategy: new LocalAuth({
        clientId: 'whatsapp-api-session'
    }),
    puppeteer: {
        headless: true,
        args: [
            '--no-sandbox',
            '--disable-setuid-sandbox',
            '--disable-dev-shm-usage',
            '--disable-accelerated-2d-canvas',
            '--no-first-run',
            '--no-zygote',
            '--single-process',
            '--disable-gpu'
        ]
    }
});

// Eventos do WhatsApp Client
