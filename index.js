/**
 * @route POST /send-message
 * @description Envia uma mensagem para um n√∫mero de WhatsApp.
 * @body {string} number - N√∫mero de telefone (com DDD, pode ser sem o 55).
 * @body {string} message - Mensagem a ser enviada.
 * @returns {Object} Detalhes do envio da mensagem.
 *
 * @example
 * curl -X POST http://localhost:3000/send-message \
 *   -H "Content-Type: application/json" \
 *   -d '{"number": "11999999999", "message": "Ol√°!"}'
 */
app.post('/send-message', async (req, res) => {
    try {
        const { number, message } = req.body;

        // Valida√ß√µes
        if (!number || !message) {
            return res.status(400).json({
                success: false,
                error: 'N√∫mero e mensagem s√£o obrigat√≥rios'
            });
        }

        if (!clientReady) {
            return res.status(503).json({
                success: false,
                error: 'WhatsApp n√£o est√° conectado. Verifique o QR code.'
            });
        }

        // Formatar n√∫mero
        const formattedNumber = formatPhoneNumber(number);
        
        console.log(`üì§ Tentando enviar mensagem...`);
        console.log(`üì± Para: ${formattedNumber}`);
        console.log(`üí¨ Mensagem: ${message}`);

        // Verificar se o n√∫mero √© v√°lido no WhatsApp
        console.log(`üîç Verificando se o n√∫mero est√° registrado no WhatsApp...`);
        const isRegistered = await whatsappClient.isRegisteredUser(formattedNumber);
        console.log(`‚úÖ N√∫mero registrado: ${isRegistered}`);
        
        if (!isRegistered) {
            console.log(`‚ùå N√∫mero ${formattedNumber} n√£o est√° registrado no WhatsApp`);
            return res.status(400).json({
                success: false,
                error: 'N√∫mero n√£o est√° registrado no WhatsApp',
                formattedNumber: formattedNumber
            });
        }

        // Verificar se existe um chat com esse contato
        console.log(`üîç Verificando chat existente...`);
        try {
            const chat = await whatsappClient.getChatById(formattedNumber);
            console.log(`üí¨ Chat encontrado: ${chat.name || 'Sem nome'}`);
        } catch (chatError) {
            console.log(`‚ö†Ô∏è Chat n√£o encontrado, mas tentando enviar mesmo assim...`);
        }

        // Enviar mensagem
        console.log(`üì§ Enviando mensagem agora...`);
        const result = await whatsappClient.sendMessage(formattedNumber, message);
        
        console.log(`‚úÖ Mensagem enviada!`);
        console.log(`üì® ID da mensagem: ${result.id.id}`);
        console.log(`üì® Status: ${result.ack}`); // 0=erro, 1=enviado, 2=recebido, 3=lido
        
        res.json({
            success: true,
            messageId: result.id.id,
            timestamp: new Date().toISOString(),
            to: number,
            formattedNumber: formattedNumber,
            message: message,
            ackStatus: result.ack,
            ackDescription: getAckDescription(result.ack)
        });

    } catch (error) {
        console.error('‚ùå Erro ao enviar mensagem:', error);
        res.status(500).json({
            success: false,
            error: 'Erro interno do servidor',
            details: error.message
        });
    }
});

