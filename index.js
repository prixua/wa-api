// Fun√ß√£o para formatar n√∫mero de telefone
function formatPhoneNumber(number) {
    console.log(`üîß Formatando n√∫mero original: ${number}`);
    
    // Remove caracteres n√£o num√©ricos
    let cleanNumber = number.replace(/\D/g, '');
    console.log(`üîß N√∫mero limpo: ${cleanNumber}`);
    
    // Se n√£o come√ßar com c√≥digo do pa√≠s, adiciona c√≥digo do Brasil (55)
    if (!cleanNumber.startsWith('55') && cleanNumber.length === 11) {
        cleanNumber = '55' + cleanNumber;
        console.log(`üîß Adicionado c√≥digo do pa√≠s: ${cleanNumber}`);
    }
    
    // Adiciona @c.us no final para WhatsApp
    const formattedNumber = cleanNumber + '@c.us';
    console.log(`üîß N√∫mero final formatado: ${formattedNumber}`);
    
    return formattedNumber;
}

